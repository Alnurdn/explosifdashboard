                    <h3 align="center">Deskripsi</h3>
                    <p align="justify">Software Development (Pengembangan Perangkat Lunak) merupakan suatu lomba dibidang pengembangan perangkat lunak yang menguji kemampuan peserta dalam berkreasi untuk membuat sebuah solusi dari suatu permasalahan dalam bentuk sebuah perangkat lunak.   Lomba software development  ini difokuskan untuk aspek inovatif dan fungsi yang ditawarkan harus berguna bagi para pelajar (TK,SD,SMP,SMA, atau MAHASISWA).</p>
                    <p align="justify">Tim peserta diharapkan dapat membuat sebuah software yang inovatif. Software tersebut haruslah bermanfaat bagi pelajar(tidak termasuk kecurangan).  Hasil yang kami harapkan tidak hanya berupa argumentasi atau ide saja namun disertakan dengan data sehingga nantinya software tersebut dapat dioperasikan hingga level tertentu dan  kemanfaatannya dapat diukur. Tidak ada batasan platform dalam lomba.</p>

                    <h3 align="center">Syarat dan Ketentuan Lomba</h3>
                    <p>1. Peserta merupakan mahasiswa program diploma atau sarjana di perguruan tinggi negeri atau swasta di Indonesia</p>
                    <p>2. Peserta adalah mahasiswa berstatus resmi yang dibuktikan dengan Kartu Tanda Mahasiswa yang masih berlaku atau Surat Keterangan Resmi dari perguruan tinggi
</p>
                    <p>3. Peserta merupakan kelompok yang terdiri dari maksimal 4 orang dan dipimpin oleh seorang ketua</p>
                    <p>4. Setiap kelompok harus dari universitas yang sama namun diperbolehkan berasal dari jurusan atau departemen yang berbeda</p>
                    <p>5. Software yang dilombakan haruslah inovatif</p>
                    <p>6. Software yang dilombakan tidak sedang/belum pernah memenangi  penghargaan di lomba manapun</p>
                    <p>7. Software tidak mengandung SARA,kekerasan dan hasutan negatif</p>
                    <p>8. Software yang melanggar syarat dan ketentuan akan didiskualifikasi</p>

                    <h3 align="center">Kriteria Penilaian</h3>
                    <h4>Babak Penyisihan :</h4>
                    <p>1. Kreatif,solusional, dan inovatif</p>
                    <p>2. Bermanfaat bagi pelajar</p>
                    <p>3. Potensi penggunaan aplikasi oleh pelajar</p>
                    <p>4. Kemudahan dalam penggunaan aplikasi</p>
                    <p>5. Proses pengembangan aplikasi yang mengikuti metode pengembangan software yang baik</p>
                    <h4>Babak Final :</h4>
                    <p>1. Kemampuan peserta dalam presentasi didepan juri</p>
                    <p>2. Kemampuan peserta dalam menjawab pertanyaan juri</p>
                    <p>3. Kemampuan peserta dalam menguasai karya yang dikembangkannya</p>

                    <h3 align="center">Bentuk Lomba</h3>
                    <h4>Babak Penyisihan :</h4>
                    <p>1. Peserta mengirimkan proposal rancangan pengembangan perangkat lunak ke alamat email lomba@explosiftelkomuniversity.com dengan format yang sudah  ditentukan</p>
                    <p>2. Isi proposal haruslah lengkap, mengikuti aturan pengajuan proposal resmi yang meliputi </p>
                    <p style="margin-left:5em;">I Judul Aplikasi</p>
                    <p style="margin-left:5em;">II Latar belakang</p>
                    <p style="margin-left:5em;">III Tujuan dan Manfaat</p>
                    <p style="margin-left:5em;">IV Batasan</p>
                    <p style="margin-left:5em;">V Metode</p>
                    <p style="margin-left:5em;">VI Analisa dan Desain</p>
                    <p style="margin-left:5em;">VII Implementasi</p>
                    <p style="margin-left:5em;">VIII Show Case </p>
                    <p style="margin-left:5em;">IX Dokumentasi</p>
                    <h4>Babak Final :</h4>
                    <p>1. Presentasi proposal dengan demo karya yang telah dibuat</p>
                    <p>2. Sesi Tanya jawab dengan juri</p>

                    <h3 align="center">Persyaratan Khusus</h3>
                    <p>1. Setiap tim hanya boleh mengajukan 1 karya</p>
                    <p>2. Apabila karya merupakan sebuah  karya yang dikembangkan, berikan penjelasan mengenai inovasi apa yang ditambahkan/dirubah dan berikan perbedaan dengan karya sebelumnya secara jelas</p>
                    <p>3. Hasil keputusan juri tidak dapat diganggu gugat</p>

                    <h3 align="center">Tata Cara Pendaftaran</h3>
                    <p>1. Peserta lomba terlebih dahulu melakukan registrasi di alamat </p>
                    <p>2. Setelah melakukan pendaftaran, peserta lomba dikenakan biaya pendaftaran sebesar Rp 100.000-, untuk setiap tim dikirim ke rekening berikut</p>
                    <p>3. Setelah melakukan pembayaran, kirimkan data konfirmasi pembayaran dengan format :</p>
                    <p style="margin-left:5em;">-Nama Lomba         :</p>
                    <p style="margin-left:5em;">-Nama Tim       :</p>
                    <p style="margin-left:5em;">-Nama Ketua     :</p>
                    <p style="margin-left:5em;">-Nama Anggota       :</p>
                    <p style="margin-left:5em;">-Asal Universitas   :</p>
                    <p style="margin-left:5em;">-Jumlah Transfer    :</p>
                    <p style="margin-left:5em;">-Tanggal Transfer   :</p>
                    <p style="margin-left:5em;">-CP ketua       :</p>
                    <p>4. Kirim data tersebut ke alamat e-mail lomba@explosiftelkomuniversity.com dengan subjek [KONFIRMASI]_[NAMA TIM]_[NAMA KETUA]_[NAMA LOMBA]</p>
                    <p>5. Setelah mengirimkan konfirmasi pembayaran, tunggu hingga panitia mengirimkan balasan  konfirmasi bahwa peserta sudah terdaftar</p>
